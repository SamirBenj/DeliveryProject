class Product {
  String nomProduit;
  int prix;
  String imageProduit;
  bool valeurBool;
  String description;
  Product(String nomProduit, int prix, String imageProduit, bool valeurBool,
      String description) {
    this.nomProduit = nomProduit;
    this.prix = prix;
    this.imageProduit = imageProduit;
    this.valeurBool = valeurBool;
    this.description = description;
  }
}
